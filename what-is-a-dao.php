            <p class="text-body-h4">Why do we need DAOs?</p>
            <p>DAO’s allow us to do things with our organizations that would otherwise be financially prohibitive for the majority of people. Most people don’t have the resources to hire the team of lawyers needed to routinely modify their organization governance systems, send out new shares each day, or to set up novel, more inclusive, more dynamic organization systems that best meet their needs. Before DAOs came along, the majority of humanity was restricted to the narrow range of commonly used organizational styles which may not reflect their values.</p>
            <p>With Hypha’s tools, DAOs are built for governance flexibility and provide communities with a diverse range of 'governance primitives' that allow communities to create governance systems suited specifically to their case at minimal cost. So, while "voting" typically comes with the assumption of 'majority rule', groups can choose to tweak this anyway they would like. From consent to consensus, from super-majority to respected-minority, from anarchy to benevolent dictatorship and anything in between, the DAO is an open governance environment and voting is but one way that groups can make decisions and align while using a DAO.</p>
            <p>Also with DAOs you don’t need to trust anyone else in the group, lawyers, or the authority of nation states. You only need to trust the DAO’s code, which is 100% transparent and verifiable by anyone, as it is recorded on the blockchain. Interestingly this actually increases trust within the group and helps groups become more open to welcome new people, as the protocols are designed to ensure that any potential community harm is minimized.</p>
